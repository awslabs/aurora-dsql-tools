    /// `is_alter_sequence_owned_by` must match the OWNED BY line for the right
    /// sequence regardless of keyword case, optional `IF EXISTS`, and trailing
    /// whitespace, while ignoring lines belonging to a different sequence.
    #[test]
    fn alter_sequence_owned_by_matching() {
        let seq = (Some("public".to_string()), "t_id_seq".to_string());
        let other = (Some("public".to_string()), "u_id_seq".to_string());

        // Plain match.
        assert!(is_alter_sequence_owned_by(
            "ALTER SEQUENCE public.t_id_seq OWNED BY public.t.id;",
            &seq
        ));
        // Case-insensitive on keywords.
        assert!(is_alter_sequence_owned_by(
            "alter sequence public.t_id_seq owned by public.t.id",
            &seq
        ));
        // Mixed case + IF EXISTS variant.
        assert!(is_alter_sequence_owned_by(
            "ALTER SEQUENCE IF EXISTS public.t_id_seq OWNED BY public.t.id;",
            &seq
        ));
        assert!(is_alter_sequence_owned_by(
            "alter sequence if exists public.t_id_seq owned by public.t.id",
            &seq
        ));
        // Different sequence -> no match (don't drop unrelated lines).
        assert!(!is_alter_sequence_owned_by(
            "ALTER SEQUENCE public.u_id_seq OWNED BY public.u.id;",
            &seq
        ));
        assert!(is_alter_sequence_owned_by(
            "ALTER SEQUENCE public.u_id_seq OWNED BY public.u.id;",
            &other
        ));
        // Not an OWNED BY statement at all.
        assert!(!is_alter_sequence_owned_by(
            "ALTER SEQUENCE public.t_id_seq RESTART WITH 100;",
            &seq
        ));
        // Not an ALTER SEQUENCE.
        assert!(!is_alter_sequence_owned_by(
            "CREATE SEQUENCE public.t_id_seq",
            &seq
        ));
    }

