    /// Finding #4: in fix mode the diagnostic's `statement` field must
    /// reflect the ORIGINAL CREATE TABLE text (the integer-NOT-NULL column),
    /// not the post-rewrite identity-column form. The rewrite is implied by
    /// `FixResult::FixedWithWarning`.
    #[test]
    fn fix_diagnostic_statement_is_original_text() {
        let mut parts = vec![
            (
                1,
                "CREATE TABLE public.t (id integer NOT NULL, x text)".into(),
            ),
            (2, "CREATE SEQUENCE public.t_id_seq CACHE 1".into()),
            (
                3,
                "ALTER TABLE ONLY public.t ALTER COLUMN id \
                 SET DEFAULT nextval('public.t_id_seq'::regclass)"
                    .into(),
            ),
        ];
        let mut diags = Vec::new();
        fix_serial_idioms(&mut parts, &mut diags);
        assert_eq!(diags.len(), 1, "expected one diagnostic: {diags:?}");
        // Original text mentions the INTEGER column the message describes.
        assert!(
            diags[0].statement.contains("integer NOT NULL"),
            "expected original CREATE TABLE text in diagnostic.statement, got: {}",
            diags[0].statement
        );
        // And does NOT pre-leak the rewritten identity form.
        assert!(
            !diags[0].statement.contains("IDENTITY"),
            "diagnostic.statement should not contain the rewritten IDENTITY clause: {}",
            diags[0].statement
        );
        // The rewrite still happened — parts[0] now carries the identity column.
        assert!(parts[0].1.contains("IDENTITY"));
    }
